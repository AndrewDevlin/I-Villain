// Villain constructor
function Villain(name, img, motive, strength, speed, wits, victory, costume, powers, iceCream) {
  this.villainName = name;
  this.img = img;
  this.motive = motive;
  this.strength = strength;
  this.speed = speed;
  this.wits = wits;
  this.victory = victory;
  this.costume = costume;
  this.powers = powers;
  this.iceCream = iceCream;
}

// Hero constructor
function Hero(name, img, strength, speed, wits) {
  this.heroName = name;
  this.img = img;
  this.strength = strength;
  this.speed = speed;
  this.wits = wits;
}
